# signs used in basic maths operations such as add, substract
# multiply or divide are called operators.
# as they help us in arithmetic they are called Arithmetic operators

# adds numbers
print(3 + 2)
# substracts numbers
print(3 - 2)
# multiplies numbers
print(3 * 2)
# divides numbers. Might give answer in float
print(3 / 2)
# divides number. always gives answer in int
print(3 // 2)
# returns remainder
print(3 % 2)
# raises to the power of or exponential
# 3 is raised to the power of 2 which is 9
print(3 ** 2)