# Comparsion means to compare if some value is larger or smaller
# operators used to evaluate comparsion statements are called comparsion operators

# below are the common camparsion operators
# (< , >) less than, greater than
# (== , !=) equal to, not equal to
# (=< , >=) less than or equal to, greater than or equal to

# examples
print(2 < 3)
print(2 > 3)
print(2 == 3)
print(2 != 3)
print(2 <= 3)
print(2 >= 3)